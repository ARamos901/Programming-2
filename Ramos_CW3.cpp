#include <iostream>
using namespace std;

int main() {
    
    const int MIN_NUM=1,
    MAX_NUM=10;
    
    
    int num;
    
    cout<<"Number Number Squared \n";
    cout<<"-----------------------\n";
    
    
    for (num=MIN_NUM;num<=MAX_NUM;num++)
        cout<<num<<"\t\t"<<(num*num)<<endl;
    
    
    
    
    return 0;
}
